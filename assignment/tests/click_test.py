import click

@click.group()
def cli():
    pass

@cli.command()
@click.argument("name")
def hello(name):
    click.echo(f"Hello, {name}!")

@cli.command()
@click.option("--count", default=1, help="repeat")
def ping(count):
    for i in range(count):
        click.echo(f"Ping {i+1}")

if __name__ == "__main__":
    cli()
